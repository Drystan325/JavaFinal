        // TODO add your handling code here:
        String firstname = jTextField1.getText();
        String lastname = jTextField2.getText();
        String username = jTextField3.getText();
        String password = jPasswordField1.getText();
        String po = "staff";
        String s = "pending";
        if(firstname.equals("")|| lastname.equals("")|| username.equals("")|| password.equals("")){
            JOptionPane.showMessageDialog(null, "Field is empty! Please fill in the field!");
            
            
        }else{ try {
            String query1 = "INSERT INTO user(first_name, last_name, username, password,position, status) VALUES (?,?,?,?,?,?)";
            st = connection.connectDB().prepareStatement(query1);
            st.setString(1, firstname);
            st.setString(2, lastname);
            st.setString(3, username);
            st.setString(4, password);
            st.setString(5, po);
            st.setString(6, s);

            
            if (st.executeUpdate() != 0) {
                JOptionPane.showMessageDialog(null, "User has been added!");
                LOGIN l = new LOGIN();
                l.show();
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "ERROR!");
            }

        } catch (Exception e) {
            System.out.println(e);

        }}

       
